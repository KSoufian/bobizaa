let handler = async (m) => {
    const jokes = [
        "مرة واحد راح للدكتور قاله عندك ايه؟ قاله عندي كحة، قاله الدكتور اكحّ، قاله اكحّ ايه؟",
        "مرة واحد طلع على القمر، لقي القمر منور، قال له انت هتشغلني قمر صناعي؟",
        "مرة واحد بيسأل صاحبه: ايه الفرق بين الفيل والنملة؟ قاله الفرق كبير يا غبي.",
        "مرة واحد دخل محل ذهب، قال له عندك ذهب؟ قال له عندي ذهب. قال له طيب هات شوية جواهر."
    ];

    const joke = jokes[Math.floor(Math.random() * jokes.length)];
    await m.reply(joke);
};

handler.command = ['نكتة'];

module.exports = handler;
